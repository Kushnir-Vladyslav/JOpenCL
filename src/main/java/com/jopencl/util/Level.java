package com.jopencl.util;

/**
 * Enumeration representing different priority or intensity levels.
 * Used for task prioritization, energy consumption levels, and other hierarchical classifications.
 *
 * @author Vladyslav Kushnir
 * @version 1.0
 * @since 2025-08-13
 */
public enum Level {
    /**
     * Low priority or intensity level
     */
    LOW,

    /**
     * Medium priority or intensity level
     */
    MED,

    /**
     * High priority or intensity level
     */
    HIGH
}
